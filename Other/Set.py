num1 = { 1,2,3,4,5}  #ay vava lakta hoba 
num2 = set([4,5,6])  # set deya ay vava laka jaii. tahola set ar moto dakaba.
num2.add(7)  #add dela 7 ta num2 a add hoya jaba.
num2.remove(7)  #remove dela 7 jode num2 a thaka tahola 7 ta remove hoya jaba.
print(4 in num2)  #4 num2 ta asa kina ayta bujayta ayta use kora hoy.
print(4 not in num2)  #4 num2 ta naii ata bujayta laka hoy. 
print(num1 | num2)  #num1 ar num2 ay 2 ta aksta print hoba.
print(num1 & num2)  #num1 ar num2 ar modda jata common asa oyta print korba.
print(num1 - num2)  #num1 thaka num2 maynas korta hoba. num1 a jata asa oyta thaka num2 beog korba.
